public class bitmunapulation {
    public static void main(String args[]){
        // bitwise operator &,|,^,<<,>>
        System.out.println(5|6); //or operation
        System.out.println(5&7); //and operation
        System.out.println((5^7)); //exor operation
        System.out.println(5<<2); // left shift  a*2^b
        System.out.println(5>>2); // right shift   a/2^b



    }
}
